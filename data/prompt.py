def build_prompt(
    question: str,
    context: str,
) -> str:
    return f"""Твојата задача е да КОПИРАШ точно дел од текстот од документот. НЕ смееш да менуваш ниту еден збор.

Документ: {context}

Прашање: {question}

СТРОГО ПРАВИЛО:
- КОПИРАЈ само зборовите од документот
- НЕ додавај ниту еден збор
- НЕ менувај ниту еден збор
- НЕ објаснувај ништо
- НЕ резимирај ништо
- НЕ парафразирај ништо
- Ако нема одговор, напиши само "Нема."

ПРИМЕР:
Ако во документот пишува "Марко е висок 180 сантиметри" и прашањето е "Колку е висок Марко?", одговорот мора да биде ТОЧНО: "180 сантиметри"

КОПИРАЈ ТОЧНО ОД ДОКУМЕНТОТ:"""
